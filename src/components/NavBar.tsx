import React, { useState, useEffect } from "react";
import { Navbar, Container, Nav, NavDropdown, NavLink } from "react-bootstrap";
import { Links } from "./Links";
import "../App.css";
import logo from "../assets/img/logo.svg";
import navIcon1 from "../assets/img/nav-icon1.svg";
import navIcon2 from "../assets/img/nav-icon2.svg";
import navIcon3 from "../assets/img/nav-icon3.svg";

export const NavBar = () => {
  const [activeLink, setActiveLink] = useState("home");
  const [scrolled, setScrolled] = useState(false);

  useEffect(() => {
    const onScroll = () => {
      if (window.scrollY > 50) {
        setScrolled(true);
      } else {
        setScrolled(false);
      }
    };

    window.addEventListener("scroll", onScroll);

    return () => window.removeEventListener("scroll", onScroll);
  }, []);

  const onUpdateActiveLink = (value: string) => {
    console.log(value);
    setActiveLink(value);
  };

  return (
    <Navbar expand="lg" className={scrolled ? "scrolled" : ""}>
      <Container>
        <Navbar.Brand href="#home">
          <img src={logo} alt="Logo" />
        </Navbar.Brand>
        <Navbar.Toggle aria-controls="basic-navbar-nav">
          <span className="navbar-toggler-icon"></span>
        </Navbar.Toggle>
        <Navbar.Collapse id="basic-navbar-nav">
          <Nav className="me-auto">
            <Links active={activeLink === "home"} href={"home"} title={"Home"} callback={onUpdateActiveLink} />
            <Links active={activeLink === "aboutMe"} href={"aboutMe"} title={"About Me"} callback={onUpdateActiveLink} />
            <Links active={activeLink === "skills"} href={"skills"} title={"Skills"} callback={onUpdateActiveLink} />
            <Links active={activeLink === "projects"} href={"projects"} title={"Projects"} callback={onUpdateActiveLink} />
            <Links active={activeLink === "contact"} href={"contact"} title={"Contact"} callback={onUpdateActiveLink} />
          </Nav>
          <span className="navbar-text">
            <div className="social-icon">
              <a href="#">
                <img src={navIcon1} alt="" />
              </a>
              <a href="#">
                <img src={navIcon2} alt="" />
              </a>
              <a href="#">
                <img src={navIcon3} alt="" />
              </a>
            </div>
            <button className="vvd" onClick={() => console.log("connect")}>
              <span>Let's Connect</span>
            </button>
          </span>
        </Navbar.Collapse>
      </Container>
    </Navbar>
  );
};
